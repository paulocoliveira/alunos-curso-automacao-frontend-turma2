def multiplicacao_quadatrica(a, b):
    quadrado_a = a ** 2
    quadrado_b = b ** 2
    return quadrado_a * quadrado_b

print(multiplicacao_quadatrica(2, 3))
print(multiplicacao_quadatrica(4, 5))